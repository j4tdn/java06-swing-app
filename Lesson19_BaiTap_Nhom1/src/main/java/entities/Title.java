package entities;

import java.util.Objects;

/**
 *
 * @author SMILE ^^
 */
public class Title {

    private Integer id;
    private String name;

    public Title() {
    }

    public Title(Integer id, String name) {
        this.id = id;
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Title [id=" + id + ", name=" + name + "]";
    }

    @Override
    public int hashCode() {
        return Objects.hash(getId());
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null || !(obj instanceof Title)) {
            return false;
        }

        Title title = (Title) obj;
        return Objects.equals(title.getId(), getId());
    }

}
